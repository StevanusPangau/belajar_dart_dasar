void main() {
  /**
   * == yaitu sama dengan
   * != yaitu tidak sama dengan
   * > yaitu lebih dari
   * < yaitu kurang dari
   * >= yaitu lebih dari sama dengan
   * <= yaitu kurang dari sama dengan
   */

  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('evan' == 'evan');
  print('evan' != 'evan');
}
