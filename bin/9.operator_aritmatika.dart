void main() {
  /**
   * + yaitu pertambahan
   * - yaitu pengurangan
   * -expresion yaitu negatif
   * * yaitu kali
   * / yaitu bagi, hasil double
   * ~/ yaitu bagi, hasil int
   * % yaitu sisa bagi
   */

  var angka1 = 10;
  var angka2 = 3;

  print(angka1 + angka2);
  print(angka1 - angka2);
  print(angka1 * angka2);
  print(angka1 / angka2);
  print(angka1 ~/ angka2);
  print(angka1 % angka2);
}
