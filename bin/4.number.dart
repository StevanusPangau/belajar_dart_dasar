void main() {
  // Di dart hanya memiliki dua number yaitu int dan double
  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  // num adalah tipe data number yang bisa merubah int ke double atau sebaliknya ibarat seperti casting atau parsing
  num number = 10;
  print(number);

  number = 10.5;
  print(number);
}
